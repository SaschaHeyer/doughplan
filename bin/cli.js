#!/usr/bin/env node
// doughplan CLI — print a bake-day plan from a JSON file of products + orders.
//
//   doughplan plan <file.json> --date 2026-06-20 --time 08:00
//   cat bakery.json | doughplan plan --date 2026-06-20
//
// Full visual planner, labels, packing lists & cloud sync: https://doughplan.com

import { readFileSync } from "node:fs";
import { buildPlan } from "../src/index.js";

const args = process.argv.slice(2);

function flag(name, def) {
  const i = args.indexOf(`--${name}`);
  return i >= 0 && args[i + 1] ? args[i + 1] : def;
}

function fmtTime(d) {
  return d.toLocaleString(undefined, { weekday: "short", hour: "2-digit", minute: "2-digit" });
}
function kg(g) {
  return g >= 1000 ? `${(g / 1000).toFixed(2)} kg` : `${Math.round(g)} g`;
}

function help() {
  console.log(`doughplan — open-source bake-day planning engine

Usage:
  doughplan plan <file.json> [--date YYYY-MM-DD] [--time HH:MM]
  cat bakery.json | doughplan plan [--date YYYY-MM-DD] [--time HH:MM]

<file.json> shape:
  { "products": [ { "name", "unitWeightG", "ingredients":[{"name","pct","isFlour","allergen"}], "schedule":[{"label","offsetMin","bake"}] } ],
    "orders":   [ { "date":"YYYY-MM-DD", "items":[{"product","qty"}] } ] }

Don't want to write JSON? Use the free visual planner at https://doughplan.com/app`);
}

function main() {
  const cmd = args[0];
  if (!cmd || cmd === "-h" || cmd === "--help" || cmd === "help") return help();
  if (cmd !== "plan") {
    console.error(`Unknown command: ${cmd}\n`);
    return help();
  }

  // Input file is the first non-flag arg after "plan"; else read stdin.
  const fileArg = args.slice(1).find((a) => !a.startsWith("--") && !isFlagValue(a));
  let raw;
  if (fileArg) {
    raw = readFileSync(fileArg, "utf8");
  } else {
    raw = readFileSync(0, "utf8"); // stdin
  }

  const data = JSON.parse(raw);
  const products = data.products || [];
  const orders = data.orders || [];

  // Default the date to the first order's date if not given.
  const date = flag("date", orders[0]?.date);
  const time = flag("time", "07:00");
  if (!date) {
    console.error("No --date given and no orders to infer it from.");
    process.exit(1);
  }

  const plan = buildPlan({ products, orders, date, bakeTime: time });

  if (!plan.productLines.length) {
    console.log(`No orders found for ${date}.`);
    return;
  }

  const line = "─".repeat(56);
  console.log(`\n🥖  DoughPlan — bake day ${date}`);
  console.log(`    ${plan.totals.units} units · ${kg(plan.totals.doughG)} dough · ${kg(plan.totals.flourG)} flour · out of oven ${time}\n`);

  console.log(`⏱  SCHEDULE (planned backward from out-of-oven)`);
  console.log(line);
  for (const s of plan.schedule) {
    const who = s.items.map((i) => `${i.product} ×${i.units}`).join(", ");
    const mark = s.bake ? "🔥" : "  ";
    console.log(`${mark} ${fmtTime(s.at).padEnd(16)} ${s.label}`);
    if (plan.productLines.length > 1) console.log(`        ${who}`);
  }

  console.log(`\n⚖️  FORMULAS`);
  console.log(line);
  for (const f of plan.formulas) {
    console.log(`${f.product} ×${f.units}  (${f.hydration}% hydration${f.allergens.length ? " · " + f.allergens.join(", ") : ""})`);
    for (const i of f.ingredients) {
      console.log(`     ${i.name.padEnd(24)} ${String(i.pct + "%").padStart(6)}   ${kg(i.grams)}`);
    }
    console.log("");
  }

  console.log(`🛒  SHOPPING LIST (aggregated across all products)`);
  console.log(line);
  for (const s of plan.shopping) {
    console.log(`     ${s.name.padEnd(24)} ${kg(s.grams)}`);
  }

  console.log(`\n────\nGenerated by doughplan (open source). The full visual planner —`);
  console.log(`labels, packing lists, multi-bake-day & cloud sync — lives at https://doughplan.com\n`);
}

function isFlagValue(a) {
  const i = args.indexOf(a);
  return i > 0 && args[i - 1].startsWith("--");
}

main();
